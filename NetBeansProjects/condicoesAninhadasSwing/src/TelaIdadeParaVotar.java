    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed
       int ano_nasc = Integer.parseInt(txtAno_nasc.getText());
       int ano_atual = Year.now().getValue();
       int idade = ano_atual - ano_nasc;
       lblIdade.setText(Integer.toString(idade));
       
       if(ano_nasc > ano_atual){
            lblSituacao.setText("ERRO! ANO DE NASCIMENTO MAIOR QUE O ANO ATUAL");
       }else if(idade < 16){
           String nao_voto = "você não vota";
           lblSituacao.setText(nao_voto);
       }else if ( ( idade >= 16 && idade < 18 ) || (idade > 70) ){
           String voto_opcional = "VOTO OPCIONAL";
           lblSituacao.setText(voto_opcional);
       }else if (idade >= 18 && idade <= 70){
           String voto_obrigatorio = "VOTO OBRIGATÓRIO";
           lblSituacao.setText(voto_obrigatorio);
       }
    }//GEN-LAST:event_btnVerificarActionPerformed

  
