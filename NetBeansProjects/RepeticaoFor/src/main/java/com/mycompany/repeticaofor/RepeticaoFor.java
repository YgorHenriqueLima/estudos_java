package com.mycompany.repeticaofor;
public class RepeticaoFor {

    public static void main(String[] args) {
        for(int i=1; i < 4; i+=1){
            System.out.println(i);
            
            for(int j=0; j < 3; j+=1){               
                System.out.println(j);
            }
            
        }
    }
}
